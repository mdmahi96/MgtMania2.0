// Helper function for debouncing
function debounce(func, delay) {
    let timer;
    return function() {
        const context = this;
        const args = arguments;
        clearTimeout(timer);
        timer = setTimeout(() => {
            func.apply(context, args);
        }, delay);
    };
}

// Button animation
function animateButton(button) {
    button.classList.add('animating');
    button.style.transform = 'scale(0.95)';
    
    setTimeout(() => {
        button.style.transform = 'scale(1)';
        setTimeout(() => {
            button.classList.remove('animating');
        }, 200);
    }, 200);
}

// Toast notification
function showToast(message, type = 'success') {
    // Remove existing toast
    const existingToast = document.querySelector('.toast');
    if (existingToast) {
        existingToast.remove();
    }
    
    // Create new toast
    const toast = document.createElement('div');
    toast.className = `toast ${type}`;
    
    const icon = document.createElement('i');
    icon.className = type === 'success' ? 'fas fa-check-circle' : 'fas fa-exclamation-circle';
    
    const messageEl = document.createElement('div');
    messageEl.className = 'toast-message';
    messageEl.textContent = message;
    
    toast.appendChild(icon);
    toast.appendChild(messageEl);
    document.body.appendChild(toast);
    
    // Show toast
    setTimeout(() => {
        toast.classList.add('show');
        
        // Hide toast after 3 seconds
        setTimeout(() => {
            toast.classList.remove('show');
            setTimeout(() => {
                toast.remove();
            }, 500);
        }, 3000);
    }, 100);
}

function showSuggestions(searchTerm) {
    searchTerm = searchTerm.trim().toLowerCase();
    
    if (searchTerm === '') {
        hideSuggestions();
        return;
    }
    
    // Get matching students
    const matchingStudents = studentsData.filter(student => {
        return student.NAME.toLowerCase().includes(searchTerm) || 
               student.ROLL.includes(searchTerm);
    });
    
    if (matchingStudents.length === 0) {
        hideSuggestions();
        return;
    }
    
    // Populate suggestions with improved presentation
    suggestionsContainer.innerHTML = '';
    
    // Limit suggestions to prevent overwhelming the user
    const maxSuggestions = 6;
    const limitedSuggestions = matchingStudents.slice(0, maxSuggestions);
    
    limitedSuggestions.forEach(student => {
        const suggestionItem = document.createElement('div');
        suggestionItem.className = 'suggestion-item';
        
        let nameHTML = student.NAME;
        if (student.NAME.toLowerCase().includes(searchTerm)) {
            // Highlight the matching part in the name
            const start = student.NAME.toLowerCase().indexOf(searchTerm);
            const end = start + searchTerm.length;
            nameHTML = 
                student.NAME.substring(0, start) + 
                `<span class="highlight">${student.NAME.substring(start, end)}</span>` + 
                student.NAME.substring(end);
        }
        
        let rollHTML = student.ROLL;
        if (student.ROLL.includes(searchTerm)) {
            // Highlight the matching part in the roll
            const start = student.ROLL.indexOf(searchTerm);
            const end = start + searchTerm.length;
            rollHTML = 
                student.ROLL.substring(0, start) + 
                `<span class="highlight">${student.ROLL.substring(start, end)}</span>` + 
                student.ROLL.substring(end);
        }
        
        // Add section info in the suggestion
        const sectionLetter = student.SECTION.includes('sec A') ? 'A' : 'B';
        
        suggestionItem.innerHTML = `
            ${nameHTML} 
            <span style="color: #64748b; font-size: 0.9em;">
                (${rollHTML} | Section ${sectionLetter})
            </span>
        `;
        
        suggestionItem.addEventListener('click', () => {
            searchInput.value = student.NAME;
            filterStudents(student.NAME);
            hideSuggestions();
        });
        
        suggestionsContainer.appendChild(suggestionItem);
    });
    
    // Show total matches if more than shown
    if (matchingStudents.length > maxSuggestions) {
        const moreResults = document.createElement('div');
        moreResults.className = 'suggestion-item';
        moreResults.style.textAlign = 'center';
        moreResults.style.fontStyle = 'italic';
        moreResults.style.color = '#64748b';
        moreResults.style.background = '#f8fafc';
        moreResults.textContent = `${matchingStudents.length - maxSuggestions} more results. Refine your search.`;
        suggestionsContainer.appendChild(moreResults);
    }
    
    // Show suggestion container with smooth animation
    suggestionsContainer.style.display = 'block';
    suggestionsContainer.style.animation = 'slideDown 0.3s forwards';
}

