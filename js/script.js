// Setup accessibility features
function setupAccessibility() {
    // Add WAI-ARIA attributes for better screen reader support
    const searchInput = document.getElementById('searchInput');
    if (searchInput) {
        searchInput.setAttribute('aria-label', 'Search students by name or ID');
        document.getElementById('searchBtn').setAttribute('aria-label', 'Search');
        document.getElementById('resetBtn').setAttribute('aria-label', 'Reset filters');
    }
    
    // Add keyboard navigation for table rows
    const tableRows = document.querySelectorAll('.students-table tbody tr');
    tableRows.forEach(row => {
        row.setAttribute('tabindex', '0');
        row.setAttribute('role', 'button');
        row.setAttribute('aria-label', `View details for ${row.cells[0].textContent}`);
        
        row.addEventListener('keydown', (event) => {
            if (event.key === 'Enter' || event.key === ' ') {
                const detailBtn = row.querySelector('.detail-btn');
                if (detailBtn) {
                    detailBtn.click();
                }
                event.preventDefault();
            }
        });
    });
}

// Call setup functions when DOM is fully loaded
window.addEventListener('load', () => {
    setupThemeSwitcher();
    setupAccessibility();
    
    // Performance optimization to enhance scrolling smoothness
    document.querySelectorAll('table, .suggestions-container')
        .forEach(el => el.style.willChange = 'transform');
    
    // Remove will-change after initial rendering
    setTimeout(() => {
        document.querySelectorAll('[style*="will-change"]')
            .forEach(el => el.style.willChange = 'auto');
    }, 2000);
});