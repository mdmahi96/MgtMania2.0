// Animate count for total students
function animateCountUp(element, start, end) {
    const duration = 1000;
    const frameDuration = 1000/60; // 60fps
    const totalFrames = Math.round(duration / frameDuration);
    const countIncrement = (end - start) / totalFrames;
    
    let currentFrame = 0;
    let currentCount = start;
    
    const animate = () => {
        currentFrame++;
        currentCount += countIncrement;
        
        if (currentFrame === totalFrames) {
            element.textContent = end;
        } else {
            element.textContent = Math.floor(currentCount);
            requestAnimationFrame(animate);
        }
    };
    
    animate();
}

function updatePagination() {
    // Calculate total pages
    const totalPages = Math.ceil(filteredStudents.length / itemsPerPage);
    const paginationContainer = document.getElementById('pagination');
    paginationContainer.innerHTML = '';

    // Define a pagination range: 2 pages before and after currentPage
    const startPage = Math.max(1, currentPage - 2);
    const endPage = Math.min(totalPages, currentPage + 2);

    // Add "First" button if needed
    if (startPage > 1) {
        const firstBtn = document.createElement('button');
        firstBtn.innerText = 'First';
        firstBtn.onclick = () => {
            currentPage = 1;
            renderStudents();
            updatePagination();
        };
        paginationContainer.appendChild(firstBtn);

        if (startPage > 2) {
            const ellipsis = document.createElement('span');
            ellipsis.innerText = '...';
            paginationContainer.appendChild(ellipsis);
        }
    }

    // Generate buttons for pages in the current range
    for (let i = startPage; i <= endPage; i++) {
        const btn = document.createElement('button');
        btn.innerText = i;

        if (i === currentPage) {
            btn.classList.add('active');
        }

        btn.onclick = () => {
            currentPage = i;
            renderStudents();
            updatePagination();
        };

        paginationContainer.appendChild(btn);
    }

    // Add "Last" button if needed
    if (endPage < totalPages) {
        if (endPage < totalPages - 1) {
            const ellipsis = document.createElement('span');
            ellipsis.innerText = '...';
            paginationContainer.appendChild(ellipsis);
        }

        const lastBtn = document.createElement('button');
        lastBtn.innerText = 'Last';
        lastBtn.onclick = () => {
            currentPage = totalPages;
            renderStudents();
            updatePagination();
        };

        paginationContainer.appendChild(lastBtn);
    }
}


