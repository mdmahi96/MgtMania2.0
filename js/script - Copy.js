function setupEventListeners() {
    // Search functionality
    searchBtn.addEventListener('click', () => {
        filterStudents(searchInput.value);
        hideSuggestions();
    });
    
    searchInput.addEventListener('keyup', (event) => {
        if (event.key === 'Enter') {
            filterStudents(searchInput.value);
            hideSuggestions();
        } else {
            // Show suggestions as user types
            showSuggestions(searchInput.value);
        }
    });
    
    // Hide suggestions when clicking outside
    document.addEventListener('click', (event) => {
        if (event.target !== searchInput && event.target !== suggestionsContainer) {
            hideSuggestions();
        }
    });
    
    // Reset button
    resetBtn.addEventListener('click', () => {
        searchInput.value = '';
        activeSection = 'all';
        updateSectionButtons();
        resetFilters();
        hideSuggestions();
    });
    
    // Section filters
    sectionABtn.addEventListener('click', () => {
        activeSection = 'A';
        updateSectionButtons();
        filterBySection('A');
    });
    
    sectionBBtn.addEventListener('click', () => {
        activeSection = 'B';
        updateSectionButtons();
        filterBySection('B');
    });
    
    allSectionsBtn.addEventListener('click', () => {
        activeSection = 'all';
        updateSectionButtons();
        resetFilters();
    });
    
    migratedBtn.addEventListener('click', () => {
        activeSection = 'migrated';
        updateSectionButtons();
        filterStudents(searchInput.value);
    });
    
    // Close modal when X is clicked
    document.querySelector('.close-modal').addEventListener('click', closeModal);
    
    // Close modal when clicking outside of it
    window.addEventListener('click', (event) => {
        const modal = document.getElementById('studentModal');
        if (event.target === modal) {
            closeModal();
        }
    });
}

function showSuggestions(searchTerm) {
    searchTerm = searchTerm.trim().toLowerCase();
    
    if (searchTerm === '') {
        hideSuggestions();
        return;
    }
    
    // Get matching students
    const matchingStudents = studentsData.filter(student => {
        return student.NAME.toLowerCase().includes(searchTerm) || 
               student.ROLL.includes(searchTerm);
    });
    
    if (matchingStudents.length === 0) {
        hideSuggestions();
        return;
    }
    
    // Populate suggestions
    suggestionsContainer.innerHTML = '';
    
    // Limit suggestions to prevent overwhelming the user
    const maxSuggestions = 8;
    const limitedSuggestions = matchingStudents.slice(0, maxSuggestions);
    
    limitedSuggestions.forEach(student => {
        const suggestionItem = document.createElement('div');
        suggestionItem.className = 'suggestion-item';
        
        let nameHTML = student.NAME;
        if (student.NAME.toLowerCase().includes(searchTerm)) {
            // Highlight the matching part in the name
            const start = student.NAME.toLowerCase().indexOf(searchTerm);
            const end = start + searchTerm.length;
            nameHTML = 
                student.NAME.substring(0, start) + 
                `<span class="highlight">${student.NAME.substring(start, end)}</span>` + 
                student.NAME.substring(end);
        }
        
        let rollHTML = student.ROLL;
        if (student.ROLL.includes(searchTerm)) {
            // Highlight the matching part in the roll
            const start = student.ROLL.indexOf(searchTerm);
            const end = start + searchTerm.length;
            rollHTML = 
                student.ROLL.substring(0, start) + 
                `<span class="highlight">${student.ROLL.substring(start, end)}</span>` + 
                student.ROLL.substring(end);
        }
        
        suggestionItem.innerHTML = `${nameHTML} (${rollHTML})`;
        
        suggestionItem.addEventListener('click', () => {
            searchInput.value = student.NAME;
            filterStudents(student.NAME);
            hideSuggestions();
        });
        
        suggestionsContainer.appendChild(suggestionItem);
    });
    
    // Show suggestion container
    suggestionsContainer.style.display = 'block';
}

function hideSuggestions() {
    suggestionsContainer.style.display = 'none';
}

function updateSectionButtons() {
    // Remove active class from all buttons
    sectionABtn.classList.remove('active');
    sectionBBtn.classList.remove('active');
    allSectionsBtn.classList.remove('active');
    migratedBtn.classList.remove('active');
    
    // Add active class to selected button
    if (activeSection === 'A') {
        sectionABtn.classList.add('active');
    } else if (activeSection === 'B') {
        sectionBBtn.classList.add('active');
    } else if (activeSection === 'migrated') {
        migratedBtn.classList.add('active');
    } else {
        allSectionsBtn.classList.add('active');
    }
    
    // Update current section text
    document.getElementById('currentSection').textContent = 
        activeSection === 'migrated' ? 'Migrated Students' :
        activeSection === 'all' ? 'All Students' : `Section ${activeSection}`;
}

function filterStudents(searchTerm) {
    searchTerm = searchTerm.trim().toLowerCase();
    
    filteredStudents = studentsData.filter(student => {
        const matchesSearch = 
            student.NAME.toLowerCase().includes(searchTerm) || 
            student.ROLL.includes(searchTerm);
            
        let matchesSection;
        if (activeSection === 'all') {
            matchesSection = true;
        } else if (activeSection === 'A' || activeSection === 'B') {
            matchesSection = student.SECTION.includes(`sec ${activeSection}`);
        } else if (activeSection === 'migrated') {
            matchesSection = student["Sl No"] === "Migrated";
        } else {
            matchesSection = false;
        }
        
        return matchesSearch && matchesSection;
    });
    
    currentPage = 1;
    displayStudents();
}

function filterBySection(section) {
    filteredStudents = studentsData.filter(student => 
        student.SECTION.includes(`sec ${section}`));
    
    // Clear search input
    if (searchInput.value !== '') {
        filterStudents(searchInput.value);
    } else {
        // Reset to first page and display
        currentPage = 1;
        displayStudents();
    }
}

function resetFilters() {
    filteredStudents = [...studentsData];
    currentPage = 1;
    displayStudents();
}

function displayStudents() {
    const tableBody = document.getElementById('studentsTableBody');
    const totalStudentsElement = document.getElementById('totalStudents');
    
    // Update total students count
    totalStudentsElement.textContent = filteredStudents.length;
    
    // Clear the table
    tableBody.innerHTML = '';
    
    // Calculate pagination
    const startIndex = (currentPage - 1) * itemsPerPage;
    const endIndex = Math.min(startIndex + itemsPerPage, filteredStudents.length);
    const studentsToDisplay = filteredStudents.slice(startIndex, endIndex);
    
    // Check if there are students to display
    if (studentsToDisplay.length === 0) {
        const noResultsRow = document.createElement('tr');
        noResultsRow.innerHTML = `
            <td colspan="4" style="text-align: center; padding: 20px;">
                No students found. Please try a different search.
            </td>
        `;
        tableBody.appendChild(noResultsRow);
    } else {
        // Add student rows
        studentsToDisplay.forEach(student => {
            const row = document.createElement('tr');
            
            // Extract section letter
            const sectionLetter = student.SECTION.includes('sec A') ? 'A' : 'B';
            
            row.innerHTML = `
                <td>${student.NAME}</td>
                <td>${student.ROLL}</td>
                <td>${sectionLetter}</td>
                <td>
                    <button class="detail-btn" data-roll="${student.ROLL}">
                        <i class="fas fa-user"></i> Details
                    </button>
                </td>
            `;
            tableBody.appendChild(row);
        });
        
        // Add event listeners to detail buttons
        document.querySelectorAll('.detail-btn').forEach(button => {
            button.addEventListener('click', () => {
                const roll = button.getAttribute('data-roll');
                showStudentDetails(roll);
            });
        });
    }
    
    // Update pagination
    updatePagination();
}

function updatePagination() {
    const paginationElement = document.getElementById('pagination');
    paginationElement.innerHTML = '';
    
    // Calculate total pages
    const totalPages = Math.ceil(filteredStudents.length / itemsPerPage);
    
    // Don't show pagination if only one page
    if (totalPages <= 1) {
        return;
    }
    
    // Previous button
    const prevButton = document.createElement('button');
    prevButton.innerHTML = '<i class="fas fa-chevron-left"></i>';
    prevButton.disabled = currentPage === 1;
    prevButton.addEventListener('click', () => {
        if (currentPage > 1) {
            currentPage--;
            displayStudents();
        }
    });
    paginationElement.appendChild(prevButton);
    
    // Page buttons
    let startPage = Math.max(1, currentPage - 2);
    let endPage = Math.min(totalPages, startPage + 4);
    
    // Adjust startPage if we're near the end
    if (endPage - startPage < 4) {
        startPage = Math.max(1, endPage - 4);
    }
    
    for (let i = startPage; i <= endPage; i++) {
        const pageButton = document.createElement('button');
        pageButton.textContent = i;
        if (i === currentPage) {
            pageButton.classList.add('active');
        }
        pageButton.addEventListener('click', () => {
            currentPage = i;
            displayStudents();
        });
        paginationElement.appendChild(pageButton);
    }
    
    // Next button
    const nextButton = document.createElement('button');
    nextButton.innerHTML = '<i class="fas fa-chevron-right"></i>';
    nextButton.disabled = currentPage === totalPages;
    nextButton.addEventListener('click', () => {
        if (currentPage < totalPages) {
            currentPage++;
            displayStudents();
        }
    });
    paginationElement.appendChild(nextButton);
}

